public class MyTerrestrialResult {
    double howManyFuel;
    int howManyDistance;

    public MyTerrestrialResult(double howManyFuel, int howManyDistance) {
        this.howManyFuel = howManyFuel;
        this.howManyDistance = howManyDistance;
    }

    public double getHowManyFuel() {
        return howManyFuel;
    }

    public void setHowManyFuel(double howManyFuel) {
        this.howManyFuel = howManyFuel;
    }

    public int getHowManyDistance() {
        return howManyDistance;
    }

    public void setHowManyDistance(int howManyDistance) {
        this.howManyDistance = howManyDistance;
    }

}
